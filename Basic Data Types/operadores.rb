somar = 1 + 1
puts "O valor somado é: #{somar}" 

multiplicar = 2 * 2
puts "O valor multiplicado é: #{multiplicar}"

exponencial = 2 ** 2
puts "O valor exponencial é: #{exponencial}" 

# ao fazer aritmética com dois inteiros em Ruby, o resultado sempre será um inteiro. 
valorInteiro = 17 / 5    #=> 3, not 3.4
puts "valorInteiro"

# Para obter uma resposta precisa, basta substituir um dos números inteiros da expressão por um ponto flutuante.
valorFloat = 17 / 5.0  #=> 3.4
puts valorFloat